import pickle
import argparse
import os
import sys

def analyze_results(pkl_file):
    if not os.path.exists(pkl_file):
        print(f"Error: File '{pkl_file}' not found.")
        sys.exit(1)

    try:
        with open(pkl_file, 'rb') as f:
            data = pickle.load(f)
    except Exception as e:
        print(f"Error loading pickle file: {e}")
        sys.exit(1)

    print(f"Analyzing {pkl_file}...\n")
    print(f"{'Topology':<20} | {'Accuracy':<10} | {'Avg Rounds':<12} | {'Total Rounds':<12} | {'Empty/None Rounds':<18}")
    print("-" * 80)

    for topology_entry in data:
        # Each entry is a dict with keys: 'topology_name', 'topology', 'results'
        topo_name = topology_entry.get('topology_name', 'Unknown')
        results = topology_entry.get('results', [])

        if not results:
            print(f"{topo_name:<20} | No results found.")
            continue

        valid_questions_count = 0
        correct_count = 0
        total_rounds = 0
        empty_msg_rounds_count = 0
        
        # results is a list of question results. Some might be None if processing failed.
        for q_result in results:
            if q_result is None:
                continue

            valid_questions_count += 1
            
            # 1. Percentage of right answers
            if q_result.get('is_correct', False):
                correct_count += 1

            debate_rounds = q_result.get('debate_rounds', [])
            num_rounds = len(debate_rounds)
            total_rounds += num_rounds

            # 4. Number of rounds that have any empty or None message
            for round_responses in debate_rounds:
                round_has_issue = False
                # round_responses is a list of agent response dicts
                for agent_resp in round_responses:
                    ans = agent_resp.get('answer')
                    reason = agent_resp.get('reason')
                    
                    # If process-text was used, 'reason' is replaced by 'st_embedding'
                    has_embeddings = 'st_embedding' in agent_resp
                    
                    if ans is None:
                        round_has_issue = True
                        break
                        
                    if isinstance(ans, str) and not ans.strip():
                        round_has_issue = True
                        break
                        
                    # If neither reason nor embeddings are present, it's an issue
                    if reason is None and not has_embeddings:
                        round_has_issue = True
                        break

                    # If reason IS present but empty string
                    if reason is not None and isinstance(reason, str) and not reason.strip():
                        round_has_issue = True
                        break
                
                if round_has_issue:
                    empty_msg_rounds_count += 1

        if valid_questions_count == 0:
            print(f"{topo_name:<20} | No valid questions.")
            continue

        accuracy = (correct_count / valid_questions_count) * 100
        avg_rounds = total_rounds / valid_questions_count

        print(f"{topo_name:<20} | {accuracy:<9.2f}% | {avg_rounds:<12.2f} | {total_rounds:<12} | {empty_msg_rounds_count:<18}")

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Analyze debate results from a pickle file.")
    parser.add_argument("pkl_file", type=str, help="Path to the .pkl file generated by generate-data.py")
    args = parser.parse_args()

    analyze_results(args.pkl_file)
